# Python program to compute Perimeter of Trapezoid
print("Perimeter of Trapezoid")

# input / assignment
length_1 = 20
length_2 = 10
height = 30

# compute / process
perimeter = length_1 + length_2 + height*2

# output / results
print("Length of Side A =",length_1,"m")
print("Length of Side B =",length_2,"m")
print("Height =",height,"m")
print("\n Perimeter of Trapezoid=",perimeter,"sq.m")
